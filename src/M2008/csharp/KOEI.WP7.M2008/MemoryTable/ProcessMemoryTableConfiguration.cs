using System;

namespace KOEI.WP7.M2008.MemoryTable
{
	/// <summary>
	/// プロセスメモリ上のデータテーブル(配列)の構造情報を管理するクラス
	/// </summary>
	public class ProcessMemoryTableConfiguration
	{
		/// <summary>
		/// データテーブル(配列)へのポインタアドレス
		/// </summary>
		public UInt32 PointerAddress;

		/// <summary>
		/// テーブル内の1レコードサイズ
		/// </summary>
		public UInt32 RecordSize;

		/// <summary>
		/// 最大レコード数
		/// </summary>
		public UInt32 RecordMaxLength;
	}
}
