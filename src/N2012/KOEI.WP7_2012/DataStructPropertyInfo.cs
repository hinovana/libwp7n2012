using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Reflection;

namespace KOEI.WP7_2012
{
	public class DataStructPropertyInfo
	{
		public PropertyInfo Property;
		public UInt32 nBits;
		public UInt32 MaxValue;
		public String Summary;
		public String Description;

		public DataStructPropertyInfo( PropertyInfo info, UInt32 nBits, UInt32 maxValue, String summary, String description )
		{
			this.Property = info;
			this.nBits = nBits;
			this.MaxValue = maxValue;
			this.Summary = summary;
			this.Description = description;
		}
	}
}
